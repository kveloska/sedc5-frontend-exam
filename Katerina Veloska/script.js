class Novel {
	constructor (title, author, publisher, yearOfPublication, pages, series, ISBN, review){
		this.title = title;
		this.author = author;
		this.publisher = publisher;
		this.yearOfPublication = yearOfPublication;
		this.pages = pages;
		this.series = series;
		this.ISBN = ISBN;
		this.review = review;
	}
};

class Anthology {
	constructor (title, editor, publisher, yearOfPublication, pages, stories, ISBN, review){
		this.title = title;
		this.editor = editor;
		this.publisher = publisher;
		this.yearOfPublication = yearOfPublication;
		this.pages = pages;
		this.stories = stories;
		this.ISBN = ISBN;
		this.review = review;
	}
};
 class Stories {
 	constructor (title, author){
 		this.title = title;
 		this.author = author;
 	}
 }

